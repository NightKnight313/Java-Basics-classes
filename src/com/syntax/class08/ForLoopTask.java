package com.syntax.class08;

public class ForLoopTask {

	public static void main(String[] args) {
		// A) 	Print numbers from 1 to 100 in 1 line with space
		// B) 	Print numbers from 100 to 1
		// C) 	Print even numbers from 20 to 1 (2 ways)
		// D) 	Print odd numbers between 20 and 50 (2 ways)
		
		//A)
			for(int x=1; x<=55; x++)	{
				System.out.print(x+" ");
		}
		System.out.println("______________________");
		
		//B)
			for(int b=55; b>=1; b--)	{
				System.out.print(b+" ");
		}
		System.out.println("---------------------------------------");
		
		//C)
			for(int a = 20; a>=1; a-=2) {
				System.out.print(a+ " ");
			}
			System.out.println();
		
		//C) second way
	
			
		}
		
	}

